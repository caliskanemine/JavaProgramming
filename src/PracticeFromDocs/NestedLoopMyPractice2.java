public class NestedLoopMyPractice2 {
    public static void main(String[] args) {


        for (int i = 0; i <= 3; i++) {

            for (int j = 1; j < 10; j++) {
                System.out.print(j+ " ");
                j += 2;

            }
            System.out.println();
        }







    }
}
